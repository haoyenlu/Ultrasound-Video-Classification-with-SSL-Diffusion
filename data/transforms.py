class Scale(object):
    """Rescale the input PIL.Image to the given size.
    Args:
        size (sequence or int): Desired output size. If size is a sequence like
            (w, h), output size will be matched to this. If size is an int,
            smaller edge of the image will be matched to this number.
            i.e, if height > width, then image will be rescaled to
            (size * height / width, size)
        interpolation (int, optional): Desired interpolation. Default is
            ``PIL.Image.BILINEAR``
    """

    def __init__(self, size, interpolation=Image.BICUBIC):
        assert isinstance(size, int) or (isinstance(size, tuple) and len(size) == 2)
        self.size = size
        self.interpolation = interpolation

    def __call__(self, img):
        """
        Args:
            img (PIL.Image): Image to be scaled.
        Returns:
            PIL.Image: Rescaled image.
        """
        if isinstance(self.size, int):
            w, h = img.size
            if (w <= h and w == self.size) or (h <= w and h == self.size):
                return img
            if w < h:
                ow = self.size
                oh = int(self.size * h / w)
                return img.resize((ow, oh), self.interpolation)
            else:
                oh = self.size
                ow = int(self.size * w / h)
                return img.resize((ow, oh), self.interpolation)
        else:
            return img.resize(self.size, self.interpolation)


class CenterCrop(object):
    """Crops the given PIL.Image at the center.
    Args:
        size (sequence or int): Desired output size of the crop. If size is an
            int instead of sequence like (h, w), a square crop (size, size) is
            made.
    """

    def __init__(self, size):
        if isinstance(size, numbers.Number):
            self.size = (int(size), int(size))
        else:
            self.size = size

    def __call__(self, img):
        """
        Args:
            img (PIL.Image): Image to be cropped.
        Returns:
            PIL.Image: Cropped image.
        """
        w, h = img.size
        th, tw = self.size
        x1 = int(round((w - tw) / 2.))
        y1 = int(round((h - th) / 2.))
        return img.crop((x1, y1, x1 + tw, y1 + th))




class LocalBinaryPattern(object):
    """Calculate Local Binary Pattern of the gray-scale ultrasound image"""
    def __init__(self, method = "add" , P = 8, R = 1, lbp_method="uniform"):

        assert method in ["add","concat"], "Method not implemented yet."

        self.method = method
        self.lbp_func = partial(local_binary_pattern, P = P, R = R, method=lbp_method)

    def __call__(self, img):
        lbp_img = self.lbp_func(image=img)

        if self.method == 'add':
            lbp_img = img + lbp_img

        
        elif self.method == 'concat':
            lbp_img = np.concatenate([img,lbp_img],axis=-1)

        lbp_img = np.clip(lbp_img, a_min=0, a_max=255)
        return lbp_img

    



class ExpandDims:
    """Expand Gray-Scale Image from two dimension to three dimension"""
    def __call__(self, img):
        if len(img.shape) == 2: # gray scale image
            img = np.expand_dims(img, axis=-1)
        
        return img



